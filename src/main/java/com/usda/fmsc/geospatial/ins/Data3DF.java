package com.usda.fmsc.geospatial.ins;

public class Data3DF {
    private float x, y, z;

    public Data3DF(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public float getZ() {
        return z;
    }
    
}
